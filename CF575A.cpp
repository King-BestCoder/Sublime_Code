#include <bits/stdc++.h>
using namespace std;
const int maxn=50005;
typedef long long LL;
LL s[maxn],f[maxn];

int main(int argc, char const *argv[])
{
	int n,m;
	LL k,p;
	while(~scanf("%I64d %I64d",&k,&p)){
		scanf("%d",&n);
		for(int i=0;i<n;i++)
			scanf("%I64d",&s[i]);
		scanf("%d",&m);

		int a,b;
		for(int i=0;i<m;i++){
			scanf("%d %d",&a,&b);
		}
	}	
	return 0;
}